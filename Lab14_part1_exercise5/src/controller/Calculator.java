public class Calculator {
	
	/**
	 * A method that will get the total summation from three numbers
	 * @param number1
	 * @param number2
	 * @param number3
	 * @return summation of the three numbers
	 * @return multiplication of the three numbers
	 */
	public int getSum(int number1, int number2, int number3) {
		
		return number1 + number2 + number3;
		
	}
	
	public int getMultiplication (int number1, int number2, int number3) {
		
		return number1 * number2 * number3;
		
	}

}
